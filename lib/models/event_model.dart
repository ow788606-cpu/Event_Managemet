class Event {
  final String id;
  final String title;
  final String date;
  final String time;
  final String location;
  final double price;
  final String imageUrl;
  final String description;

  Event({
    required this.id,
    required this.title,
    required this.date,
    required this.time,
    required this.location,
    required this.price,
    required this.imageUrl,
    required this.description,
  });
}
